// Java Program to Check if Given Integer is Odd or Even
public class JAVA_06_even_odd {
    public static void main(String[] args) {
        int num = 35;
        if (num % 2 == 0) {
            System.out.println("This no. is even");
        } else {
            System.out.println("This is no. is odd");
        }

    }
}
